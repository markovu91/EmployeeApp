    public void searchPersons(){
    ArrayList<Person> persons = new DBUpdater().listPersons(jTextField1.getText());
        dmodel = new DefaultTableModel();
        dmodel.setColumnIdentifiers(new Object[]{"id","name","age","address","income"});
        Object[] row = new Object[5];
        
        for(int i = 0; i < persons.size(); i++)
        {
            row[0]=persons.get(i).getId();
            row[1]=persons.get(i).getName();
            row[2]=persons.get(i).getAge();
            row[3]=persons.get(i).getAddress();
            row[4]=persons.get(i).getIncome();
            dmodel.addRow(row);
        }
       jTableDisplayPerson.setModel(dmodel);
    
    }
    

