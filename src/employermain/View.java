    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
         
        if (jTableDisplayPerson.getSelectedRow()==-1) {
            JOptionPane.showMessageDialog(null, "Person not selected!");
        }
        else{
        
        
        int result = JOptionPane.showConfirmDialog (null, "Are you sure you want to delete this employee?");
        if (result == JOptionPane.YES_OPTION) { 

            int index = jTableDisplayPerson.getSelectedRow();
            id = jTableDisplayPerson.getValueAt(index, 0).toString();
            
            if (new DBUpdater().delete(id)) {
                JOptionPane.showMessageDialog(null, "Person deleted!");
            }
            else{
            
            JOptionPane.showMessageDialog(null, "Not deleted!");
            }

        }

        }
    }//GEN-LAST:event_jButton4ActionPerformed

